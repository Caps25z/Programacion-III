    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
  String nombre = txtNombre.getText().trim();
    String email = txtEmail.getText().trim();
    String username = txtUsername.getText().trim();
    String clave = new String(txtClave.getPassword()).trim();
    
    if(nombre.isEmpty() || email.isEmpty() || username.isEmpty() || clave.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        return;
    }
    
    if(!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
        JOptionPane.showMessageDialog(this, "Email inv√°lido");
        return;
    }
    
    Usuario nuevoUsuario = new Usuario(nombre, email, username, clave);
    
    if(UsuarioDAO.registrarUsuario(nuevoUsuario)) {
        JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente!");
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Error al registrar usuario");
    }
    
    }//GEN-LAST:event_btnGuardarActionPerformed

